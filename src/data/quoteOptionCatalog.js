export const quoteOptionCatalog = {
  origins: mergeUnique(fclRateCatalog.china_origin_ports.map((item) => item.label)),
  destinationPorts: mergeUnique(fclRateCatalog.international_destination_ports.map((item) => item.label)),
  ddpCountries: mergeUnique(ddpWorldCountries),
  ddpChinaOrigins: mergeUnique(ddpChinaOrigins),
  containerTypes: ['20GP', '40GP', '40HQ', '45HQ', 'LCL'],
  amazonMarkets: ['US', 'CA', 'UK', 'DE', 'FR', 'IT', 'ES', 'NL', 'JP'],
  fbaWarehouses: amazonWarehouseCatalog.records,
}

export const getFbaWarehouseOptions = () => {
  return quoteOptionCatalog.fbaWarehouses.map((item) => ({
    value: item.code,
    label: `${item.code} (${item.market})`,
    market: item.market,
    country: item.country,
    address: item.address,
  }))
}

export const getFbaWarehouseRecord = (code) => {
  const normalized = String(code || '').trim().toUpperCase()
  return quoteOptionCatalog.fbaWarehouses.find((item) => item.code.toUpperCase() === normalized) || null
}
